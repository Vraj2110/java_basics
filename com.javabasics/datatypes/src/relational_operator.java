public class relational_operator {
    static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println(a<b);//lessthan operator <
        System.out.println(a>b);//greater than operator >
        System.out.println(a==b);//equal operator ==
        System.out.println(a!=b);//not equal !=
        System.out.println(a<=b);//less than or equal to <=
        System.out.println(a>=b);//greater than or equal to >=
    }
}
